/**
 * 查詢股票中文名稱
 * @param {string} symbol  股票代號（不含 .TW）
 * @param {string} fallback 找不到時的備用值（通常是 API 回的英文名）
 */
export function getStockName(symbol, fallback = '') {
  const clean = symbol.replace(/\.TW$/i, '').toUpperCase()
  return STOCK_NAMES[clean] || fallback
}

